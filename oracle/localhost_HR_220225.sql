SELECT RN, ID FN, LN
FROM (
    SELECT ROWNUM RN, EMPLOYEE_ID ID, FIRST_NAME FN, LAST_NAME LN
    FROM EMPLOYEES
    WHERE ROWNUM <= 2 * 10
)
WHERE RN > (2 - 1) * 10;
    